namespace Profunia.Inventory.Desktop.ClassFiles.Info
{
	internal class FormInfo
	{
		private int _formId;

		private string _formName;

		public int FormId
		{
			get
			{
				return _formId;
			}
			set
			{
				_formId = value;
			}
		}

		public string FormName
		{
			get
			{
				return _formName;
			}
			set
			{
				_formName = value;
			}
		}
	}
}
